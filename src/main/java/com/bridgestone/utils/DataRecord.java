public class DataRecord {
    private int x1;
    private int y1;
    private int x2;
    private int y2;
    private double speed;

    public DataRecord(int x1, int y1, int x2, int y2, double speed) {
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
        this.speed = speed;
    }
}
